// while loop

let i = 0;
while (i <= 10) {
    console.log(`Value of i is: ${i}`);
    i = i + 2;
}


let stars = ["Harry Potter", "Hermione", "Ronald"];

let arr = 0
while (arr < stars.length) {
    console.log(`Value is ${stars[arr]}`);
    arr = arr + 1;
}


let score = 15;

do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);


